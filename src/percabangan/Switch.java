package percabangan;

public class Switch {
    public static void main(String[] args){
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("urang lapas");
                break;
            case 2:
                System.out.println("beduaan");
                break;
            case 3:
                System.out.println("ngawani urang pacaran");
                break;
            case 4:
                System.out.println("nasi goreng tengah malam");
                break;
            case 5:
                System.out.println("urang subalah");
        }
    }
}
